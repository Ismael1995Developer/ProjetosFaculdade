    private void btnCadastrarAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAlterarActionPerformed
        if (txtNome.getText().equals(""))
        {
            JOptionPane.showMessageDialog
            (null,"Digite o nome!!!",
             "Nome",JOptionPane.ERROR_MESSAGE);
            txtNome.requestFocus();
        }else if (txtTelefone.getText().equals("(  )    -    "))
        {
            JOptionPane.showMessageDialog
            (null,"Digite o telefone!!!",
             "Telefone",JOptionPane.ERROR_MESSAGE);
            txtTelefone.requestFocus();
        }else if (btnCadastrarAlterar.getText().
                equals("Cadastrar"))
        {
            try
            {
                PreparedStatement cadastrar=
                Principal.conexao.prepareStatement
                ("INSERT INTO contatos(nome,telefone) VALUES(?,?)");
                cadastrar.setString(1,txtNome.getText().toUpperCase());
                cadastrar.setString(2,txtTelefone.getText());                    
                cadastrar.executeUpdate();
                JOptionPane.showMessageDialog(null,
                "Contato cadastrado com sucesso!!!",
                "Cadastrado",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null,
                "Erro de SQL: " + e.getMessage(),"Erro",
                JOptionPane.ERROR_MESSAGE);
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,
                "Erro: " + e.getMessage(),"Erro",
                JOptionPane.ERROR_MESSAGE);
            }
        }else if (btnCadastrarAlterar.getText().
                equals("Alterar"))
        {
            Object[] opcoes={"Sim","NÃ£o"};
            int retorno=JOptionPane.showOptionDialog
            (null,"Tem certeza que deseja alterar?",
            "Alterar",JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE,null,
            opcoes,opcoes[0]);
            if (retorno==0)
            {
                try
                {
                    PreparedStatement alterar=
                    Principal.conexao.prepareStatement
                    ("UPDATE contatos SET nome=?,telefone=? WHERE nome=? AND telefone=?");
                    alterar.setString(1,txtNome.getText().toUpperCase());
                    if (txtTelefone.getText().equals("(  )    -    "))
                        alterar.setString(2,"");
                    else
                        alterar.setString(2,txtTelefone.getText());                    
                    alterar.setString(3,nomeAtual);
                    alterar.setString(4,telefoneAtual);                    
                    alterar.executeUpdate();
                    JOptionPane.showMessageDialog(null,
                    "Contato alterado com sucesso!!!",
                    "Contato Alterado",
                    JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }catch(SQLException e)
                {
                    JOptionPane.showMessageDialog(null,
                    "Erro de SQL: " + e.getMessage(),
                    "Erro",JOptionPane.ERROR_MESSAGE);
                }catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,
                    "Erro: " + e.getMessage(),
                    "Erro",JOptionPane.ERROR_MESSAGE);
                }
            }
        }        
    }//GEN-LAST:event_btnCadastrarAlterarActionPerformed

