    private void inserir()
    {
        if (txtNome.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Você não digitou o nome!!!","Nome",JOptionPane.ERROR_MESSAGE);
        }else if (txtTelefone.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Você não digitou o telefone!!!","Telefone",JOptionPane.ERROR_MESSAGE);
        }else
        {
            try
            {
                PreparedStatement cadastrar=conectar.prepareStatement("INSERT INTO contatos(nome,telefone) VALUES(?,?)");
                cadastrar.setString(1,txtNome.getText());
                cadastrar.setString(2,txtTelefone.getText());
                cadastrar.executeUpdate();
                JOptionPane.showMessageDialog(null,"Contato cadastrado com sucesso!!!","Cadastrado",JOptionPane.INFORMATION_MESSAGE);
                limpar();
            }catch (SQLException e)
            {
                JOptionPane.showMessageDialog(null,"Erro de SQL","Erro",JOptionPane.ERROR_MESSAGE);
            }catch (Exception e)
            {
                JOptionPane.showMessageDialog(null,"Erro","Erro",JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    private void limpar()
    {
        txtNome.setText("");
        txtTelefone.setText("");
        txtNome.requestFocus();
    }

