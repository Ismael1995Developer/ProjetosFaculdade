    private void conexao()
    {
        try
        {
            Class.forName("org.postgresql.Driver");
            conectar=DriverManager.getConnection("jdbc:postgresql://Localhost:5432/agenda","postgres","ftec");
            //endereço do servidor,porta,nome do banco, usuário, senha
        }catch (ClassNotFoundException e)
        {
            JOptionPane.showMessageDialog(null,"Falha na conexão com o banco","Erro",JOptionPane.ERROR_MESSAGE);
        }catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Erro de SQL","Erro",JOptionPane.ERROR_MESSAGE);
        }catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Algum erro aconteceu!!!","Erro",JOptionPane.ERROR_MESSAGE);
        }
    }
    
